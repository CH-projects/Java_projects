    private Product[] products = new Product[100];
    private int productCount = 0;
    
    
    //Addproduct method
    private void addProduct(int code, String name, int quantity, double unitPrice) {
    Product newProduct = new Product(code, name, quantity, unitPrice);

    // Find the correct position to insert alphabetically
    int i = 0;
    while (i < productCount && products[i].getName().compareToIgnoreCase(name) < 0) {
        i++;
    } 

    // Shift right to make space
    for (int j = productCount; j > i; j--) {
        products[j] = products[j - 1];
    }

    // Insert new product
    products[i] = newProduct;
    productCount++;
}

    
//Deletebtn mathod
    private boolean deleteProduct(String name) {
    for (int i = 0; i < productCount; i++) {
        if (products[i].getName().equalsIgnoreCase(name)) {
            // Shift all products after this one to the left
            for (int j = i; j < productCount - 1; j++) {
                products[j] = products[j + 1];
            }
            products[productCount - 1] = null; // Remove reference
            productCount--;
            return true; // Product deleted
        }
    }
    return false; // Product not found
}

// search btn method
   private Product getProduct(String name) {
    for (int i = 0; i < productCount; i++) {
        if (products[i].getName().equalsIgnoreCase(name)) {
            return products[i]; // Found the product
        }
    }
    return null; // Not found
}
 
 //get highest price btn
   private Product getHighestPriceProduct() {
    if (productCount == 0) {
        return null; // No products in the array
    }

    Product highest = products[0];
    for (int i = 1; i < productCount; i++) {
        if (products[i].getUnitPrice() > highest.getUnitPrice()) {
            highest = products[i];
        }
    }
    return highest;
}

   // Update Price
   private boolean updatePrice(String name, double newPrice) {
    for (int i = 0; i < productCount; i++) {
        if (products[i].getName().equalsIgnoreCase(name)) {
            products[i].setUnitPrice(newPrice);
            return true;
        }
    }
    return false;
}
 //update quantity
   private boolean updateQuantity(String name, int newQuantity) {
    for (int i = 0; i < productCount; i++) {
        if (products[i].getName().equalsIgnoreCase(name)) {
            products[i].setQuantity(newQuantity);
            return true;
        }
    }
    return false;
}

    
    
    
    
    
    
    
