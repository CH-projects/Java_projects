    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
    String nameToDelete = txtName.getText().trim();

    if (nameToDelete.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a product name to delete.");
        return;
    }

    boolean deleted = deleteProduct(nameToDelete);
    if (deleted) {
        JOptionPane.showMessageDialog(this, "Product deleted successfully.");
    } else {
        JOptionPane.showMessageDialog(this, "Product not found.");
    }

    // Clear text fields
    txtCode.setText("");
    txtName.setText("");
    txtQuantity.setText("");
    txtUnitePrice.setText("");


    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String nameToSearch = txtName.getText().trim();

    if (nameToSearch.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a product name to search.");
        return;
    }

    Product result = getProduct(nameToSearch);
    if (result != null) {
        txtCode.setText(String.valueOf(result.getCode()));
        txtQuantity.setText(String.valueOf(result.getQuantity()));
        txtUnitePrice.setText(String.valueOf(result.getUnitPrice()));
        JOptionPane.showMessageDialog(this, "Product found.");
    } else {
        JOptionPane.showMessageDialog(this, "Product not found.");
        txtCode.setText("");
        txtQuantity.setText("");
        txtUnitePrice.setText("");
    }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpActionPerformed
        String name = txtName.getText().trim();
    String newPriceStr = txtUnitePrice.getText().trim();

    if (name.isEmpty() || newPriceStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Enter product name and new price.");
        return;
    }

    try {
        double newPrice = Double.parseDouble(newPriceStr);
        boolean updated = updatePrice(name, newPrice);
        if (updated) {
            JOptionPane.showMessageDialog(this, "Price updated successfully.");
        } else {
            JOptionPane.showMessageDialog(this, "Product not found.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid price format.");
    }
    }//GEN-LAST:event_btnUpActionPerformed

